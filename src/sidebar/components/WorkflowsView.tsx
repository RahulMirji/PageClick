function getIcon(name: string): ReactNode {
  return ICONS[name] || ICONS["zap"];
}

// ── Component ──────────────────────────────────────────────────────

interface WorkflowsViewProps {
  onRunWorkflow: (prompt: string) => void;
}

function WorkflowsView({ onRunWorkflow }: WorkflowsViewProps) {
  const [groups, setGroups] = useState<WorkflowGroup[]>([]);
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    setIsLoading(true);
    listWorkflows()
      .then(setGroups)
      .finally(() => setIsLoading(false));
  }, []);

  if (isLoading) {
    return (
      <div className="workflows-view">
        <div className="workflows-header">
          <h2 className="workflows-title">Workflows</h2>
        </div>
        <div className="workflows-loading">
          {[1, 2, 3, 4, 5, 6].map((i) => (
            <div key={i} className="workflow-skeleton">
              <div className="skeleton-icon-sq" />
              <div className="skeleton-lines">
                <div
                  className="skeleton-text"
                  style={{ width: `${50 + i * 8}%` }}
                />
                <div className="skeleton-text short" style={{ width: "60%" }} />
              </div>
            </div>
          ))}
        </div>
      </div>
    );
  }

  if (groups.length === 0) {
    return (
      <div className="workflows-view">
        <div className="workflows-header">
          <h2 className="workflows-title">Workflows</h2>
        </div>
        <div className="workflows-empty">
          <svg
            width="40"
            height="40"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="1.5"
            strokeLinecap="round"
            strokeLinejoin="round"
            opacity="0.3"
          >
            <polygon points="13 2 3 14 12 14 11 22 21 10 12 10 13 2" />
          </svg>
          <p>No workflows available</p>
          <span>Check back later for new templates</span>
        </div>
      </div>
    );
  }

  return (
    <div className="workflows-view">
      <div className="workflows-header">
        <h2 className="workflows-title">Workflows</h2>
        <span className="workflows-subtitle">One-tap automations</span>
      </div>
      <div className="workflows-list">
        {groups.map((group) => (
          <div key={group.category} className="workflow-group">
            <div className="workflow-group-label">
              <span className="workflow-group-emoji">
                {getCategoryEmoji(group.category)}
              </span>
              {group.category}
            </div>
            <div className="workflow-cards">
              {group.workflows.map((wf) => (
                <button
                  key={wf.id}
                  className="workflow-card"
                  onClick={() => onRunWorkflow(wf.prompt)}
                >
                  <div className="workflow-card-icon">{getIcon(wf.icon)}</div>
                  <div className="workflow-card-text">
                    <span className="workflow-card-title">{wf.title}</span>
                    <span className="workflow-card-desc">{wf.description}</span>
                  </div>
                  <svg
                    className="workflow-card-arrow"
                    width="14"
                    height="14"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                  >
                    <polyline points="9 18 15 12 9 6" />
                  </svg>
                </button>
              ))}
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}

export default WorkflowsView;
